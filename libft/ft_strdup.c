#include "libft.h"

char	*ft_strdup(const char *s)
{
	char	*new;
	int		i;
	int		j;

	i = 0;
	j = ft_strlen(s);
	new = malloc(sizeof(char) * (j + 1));
	if (!new)
		return (NULL);
	while (s[i])
	{
		new[i] = s[i];
		i++;
	}
	new[i] = '\0';
	return (new);
}
